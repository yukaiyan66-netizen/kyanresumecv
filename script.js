let language = 'zh';
const toggle = document.getElementById('languageToggle');
function renderLanguage() {
  document.documentElement.lang = language === 'zh' ? 'zh-CN' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const value = copy[language][el.dataset.i18n];
    if (value) el.textContent = value;
  });
  toggle.textContent = language === 'zh' ? 'EN' : '中';
}
toggle.addEventListener('click', () => { language = language === 'zh' ? 'en' : 'zh'; renderLanguage(); });

const observer = new IntersectionObserver(entries => entries.forEach(entry => {
  if (entry.isIntersecting) {
    entry.target.classList.add('visible');
    observer.unobserve(entry.target);
  }
}), { threshold: .10 });
document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

const metricObserver = new IntersectionObserver(entries => entries.forEach(entry => {
  if (!entry.isIntersecting) return;
  entry.target.querySelectorAll('[data-count]').forEach(el => {
    const target = Number(el.dataset.count || 0);
    const prefix = el.dataset.prefix || '';
    const suffix = el.dataset.suffix || '';
    const started = performance.now();
    const duration = 1100;
    const tick = now => {
      const p = Math.min(1, (now - started) / duration);
      const eased = 1 - Math.pow(1 - p, 3);
      el.textContent = `${prefix}${Math.round(target * eased)}${suffix}`;
      if (p < 1) requestAnimationFrame(tick);
    };
    requestAnimationFrame(tick);
  });
  metricObserver.unobserve(entry.target);
}), { threshold: .35 });
document.querySelectorAll('.metrics').forEach(el => metricObserver.observe(el));

