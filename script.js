const canvas = document.getElementById('particleCanvas');
const ctx = canvas.getContext('2d');
let particles = [];
let pointer = { x: -9999, y: -9999 };
function resizeCanvas() {
  const dpr = Math.min(devicePixelRatio || 1, 2);
  canvas.width = innerWidth * dpr;
  canvas.height = innerHeight * dpr;
  canvas.style.width = `${innerWidth}px`;
  canvas.style.height = `${innerHeight}px`;
  ctx.setTransform(dpr,0,0,dpr,0,0);
  const count = Math.min(72, Math.max(28, Math.floor(innerWidth / 24)));
  particles = Array.from({ length: count }, () => ({
    x: Math.random() * innerWidth,
    y: Math.random() * innerHeight,
    vx: (Math.random() - .5) * .18,
    vy: (Math.random() - .5) * .18,
    r: Math.random() * 1.5 + .7
  }));
}
window.addEventListener('resize', resizeCanvas);
window.addEventListener('pointermove', e => { pointer.x = e.clientX; pointer.y = e.clientY; });
window.addEventListener('pointerleave', () => { pointer.x = -9999; pointer.y = -9999; });
function drawParticles() {
  ctx.clearRect(0,0,innerWidth,innerHeight);
  for (const p of particles) {
    const dx = pointer.x - p.x;
    const dy = pointer.y - p.y;
    const d2 = dx*dx + dy*dy;
    if (d2 < 18000 && d2 > 1) {
      const force = (1 - d2 / 18000) * .0018;
      p.vx -= dx * force;
      p.vy -= dy * force;
    }
    p.x += p.vx; p.y += p.vy;
    p.vx *= .995; p.vy *= .995;
    if (p.x < -20) p.x = innerWidth + 20;
    if (p.x > innerWidth + 20) p.x = -20;
    if (p.y < -20) p.y = innerHeight + 20;
    if (p.y > innerHeight + 20) p.y = -20;
    ctx.beginPath();
    ctx.fillStyle = 'rgba(34,111,226,.55)';
    ctx.arc(p.x,p.y,p.r,0,Math.PI*2);
    ctx.fill();
  }
  for (let i=0;i<particles.length;i++) {
    for (let j=i+1;j<particles.length;j++) {
      const a=particles[i], b=particles[j];
      const dx=a.x-b.x, dy=a.y-b.y, d=Math.hypot(dx,dy);
      if (d < 118) {
        ctx.beginPath();
        ctx.strokeStyle = `rgba(32,100,205,${(1-d/118)*.18})`;
        ctx.lineWidth = .7;
        ctx.moveTo(a.x,a.y); ctx.lineTo(b.x,b.y); ctx.stroke();
      }
    }
  }
  requestAnimationFrame(drawParticles);
}
resizeCanvas();
if (!matchMedia('(prefers-reduced-motion: reduce)').matches) drawParticles();
renderLanguage();
